/*Pedir 2 números al usuario y sumarlos, restarlos, multiplicarlos y dividirlos. */

let num1 = parseInt(prompt("Digite el primer número"))
let num2 = parseInt(prompt("Digite el segundo número"))

console.log("La suma es: " + (num1 + num2)
            + "\nLa resta es: " + (num1 - num2)
            + "\nLa Multiplicación es: " + num1 * num2
            + "\nLa División es: " + num1 / num2);